public class Method {
    private double perimeter;
    private double acreage ;

    public Method(double perimeter, double acreage) {
        this.perimeter = perimeter;
        this.acreage = acreage;
    }

    public double getPerimeter() {
        return perimeter;
    }

    public double getAcreage() {
        return acreage;
    }
}

